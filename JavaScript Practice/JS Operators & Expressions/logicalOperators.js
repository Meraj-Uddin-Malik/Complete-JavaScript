console.log("Logical Operators");

let x = 5;
let y = 6;

console.log("x && y is :", x && y); // Check if both x and y are true : 6
console.log("x || y is :", x || y); // Check if either x or y is true : 5
console.log("!x is :", !x); // Logical NOT: true if x is falsy, false otherwise
console.log("!y is :", !y); // Logical NOT: true if y is falsy, false otherwise